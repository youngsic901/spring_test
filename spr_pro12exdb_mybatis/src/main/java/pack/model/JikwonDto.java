package pack.model;

public class JikwonDto {
	private String jikwonNo, jikwonName, buserName, ibsa;

	public String getJikwonNo() {
		return jikwonNo;
	}

	public void setJikwonNo(String jikwonNo) {
		this.jikwonNo = jikwonNo;
	}

	public String getJikwonName() {
		return jikwonName;
	}

	public void setJikwonName(String jikwonName) {
		this.jikwonName = jikwonName;
	}

	public String getBuserName() {
		return buserName;
	}

	public void setBuserName(String buserName) {
		this.buserName = buserName;
	}

	public String getIbsa() {
		return ibsa;
	}

	public void setIbsa(String ibsa) {
		this.ibsa = ibsa;
	}
}
